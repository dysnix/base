//!
//! ## Test Modules
//!
//! - `integration_test` - Full request flow integration tests
//! - `codec_integration` - RPC codec encoding/decoding tests
//! - `cache_integration` - Cache layer tests
//! - `config_integration` - Configuration loading and validation tests
